var yourAudio1 = document.getElementById('yourAudio1'),
  ctrl1 = document.getElementById('audioControl1');

ctrl1.onclick = function() {

  //define images while paused & played
  pause_html = '<img src="http://www.clker.com/cliparts/j/W/O/s/N/o/windows-media-player-play-button-hi.png">';

  play_html = '<img src="http://cliparts.co/cliparts/6cp/LAa/6cpLAabdi.png">';

  // Update the Button
  var pause = ctrl1.innerHTML === play_html;
  ctrl1.innerHTML = pause ? pause_html : play_html;

  // Update the Audio
  var method = pause ? 'pause' : 'play';
  yourAudio1[method]();

  // Prevent Default Action
  return false;
};

